public class Store
{
    private int numEmployees;
    private int numSold;
    private int yearsOpen;

    public void setNumEmployees(int inEmployees)
    {
        numEmployees = inEmployees;
    }
    public void setNumSold(int inNumSold)
    {
        numSold = inNumSold;
    }
    public void setYearsOpen(int inYearsOpen)
    {
        yearsOpen = inYearsOpen;
    }

    public int getNumEmployees()
    {
        return numEmployees;
    }
    public int getNumSold()
    {
        return numSold;
    }
    public int getYearsOpen()
    {
        return yearsOpen;
    }



}